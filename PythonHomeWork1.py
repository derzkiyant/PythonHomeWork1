"""
5. Напишите программу, которая принимает на вход координаты двух точек и находит расстояние между ними в 2D пространстве.
Пример:
- A (3,6); B (2,1) -> 5,09
- A (7,-5); B (1,-1) -> 7,21
"""

print('Решение задачи 5:')

x_a = int(input('Введите xA: '))
y_a = int(input('Введите yA: '))
x_b = int(input('Введите xB: '))
y_b = int(input('Введите yB: '))

distance = ((x_b - x_a)**2 + (y_b - y_a) ** 2) ** 0.5

print(f'Расстояние между точками: {round(distance, 2)}')
